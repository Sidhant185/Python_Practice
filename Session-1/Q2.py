# Q2: Calculate sum and product of two numbers
# Takes two numbers as input and outputs their sum and product

# Input: Get first number from user
n1 = int(input("Enter number 1: "))

# Input: Get second number from user
n2 = int(input("Enter number 2: "))

# Calculate sum of both numbers
sum = n1+n2

# Calculate product of both numbers
product = n1*n2

# Output: Display sum
print(sum)

# Output: Display product
print(product)
